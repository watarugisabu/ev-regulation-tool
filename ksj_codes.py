def _normalize_code(value, pad_length=None):
    """値を文字列に正規化（先頭ゼロ付きなど）"""
    if value is None:
        return None
    try:
        # 数値なら整数化してから文字列に
        if isinstance(value, float):
            if value != value:  # NaN
                return None
            value = int(value)
        s = str(value).strip()
        if s == "" or s.lower() == "nan" or s.lower() == "none":
            return None
        # 先頭ゼロ埋め
        if pad_length is not None and s.isdigit():
            s = s.zfill(pad_length)
        return s
    except Exception:
        return None


def translate_natural_park_class(code):
    """A10_003: 自然公園区分コード→日本語"""
    c = _normalize_code(code)
    if c is None:
        return ""
    return NATURAL_PARK_CLASS_CODE.get(c, f"不明コード({c})")


